public class Conditional {
    public static void main(String[] args) {
        int marks =  75;
    if(marks > 90){
        System.out.println("Excellent");
    }else if(marks > 80){
        System.out.println("Good");
    }else if(marks > 70){
        System.out.println("Fair");
    }else if(marks > 60){
        System.out.println("Meets Expectation");
    }else{
        System.out.println("Below Par");
    }
    }
}
